public class NestFor
{
    public static void main(String args[])
    {
        int row=5;
        int col=3;
        for(int i=0;i<col;i++)
        {
            for(int j=1;j<row;j++)
            {
                System.out.print("1 ");
            }
            System.out.println();
        }
    }
}