//Write a for loop that prints the even numbers from 1 to 20.
public class Q1
{
    public static void main(String args[])
    {
        System.out.println("Even numbers: ");
        for(int i=0;i<=100;i++)
        {
            if(i%2==0)
            {
                System.out.println(i);
            }
        }   
    }
}